    private void excluirArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirArquivoActionPerformed
        if(arquivoSeleciona == -1){
            showMessageDialog(null, "Nenhum arquivo selecionado!");
            return;
        }
        diretorio.apagarArquivo(arquivoSeleciona, true);
        lista.remove(arquivoSeleciona);
        listArquivos.setModel(lista);
        arquivoSeleciona = -1;
        lblNomeArquivo.setText("");
        inConteudoArquivo.setText("");
        lblContudoHD.setText(diretorio.getHd().drawConteudo());
        lblHdDisponivel.setText(Integer.toString(diretorio.getHd().tamanhoDisponivel()));
        inodesDisplay.setText("");
        lblDataCriacao.setText("");
        lblDataModificacao.setText("");
    }//GEN-LAST:event_excluirArquivoActionPerformed

