    private void salvarArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarArquivoActionPerformed
        String nome = lblNomeArquivo.getText();
        String conteudo = inConteudoArquivo.getText();
        if(nome.equals("")){
            showMessageDialog(null, "Informe o nome do arquivo!");
            return;
        }
        if(arquivoSeleciona == -1){
            diretorio.criarArquivo(nome, conteudo);
            lista.addElement(nome);
            listArquivos.setModel(lista);
        } else {
            diretorio.getInodes(arquivoSeleciona).setNome(nome);
            diretorio.editarArquivo(arquivoSeleciona, conteudo);
            lista.setElementAt(nome, arquivoSeleciona);
            listArquivos.setModel(lista);
        }
        lblContudoHD.setText(diretorio.getHd().drawConteudo());
        lblHdDisponivel.setText(Integer.toString(diretorio.getHd().tamanhoDisponivel()));
        System.out.println(diretorio.getHd());
        arquivoSeleciona = -1;
        lblNomeArquivo.setText("");
        inConteudoArquivo.setText("");
        inodesDisplay.setText("");
        lblDataCriacao.setText("");
        lblDataModificacao.setText("");
    }//GEN-LAST:event_salvarArquivoActionPerformed

